    private String formatarJSON(String json){
        
        // Remove chaves do início e fim
        json = json.trim();
        if (json.startsWith("{")) json = json.substring(1);
        if (json.endsWith("}")) json = json.substring(0, json.length() - 1);
    
        // Separa por vírgulas e formata cada campo
        String[] campos = json.split(",(?=\\\"[^\\\"]*\\\":)");
        StringBuilder resultado = new StringBuilder();
        
        for (String campo : campos) {
            campo = campo.trim();
            // Separa chave e valor
            if (campo.contains(":")) {
                String[] partes = campo.split(":", 2);
                String chave = partes[0].replace("\"", "").trim();
                String valor = partes[1].trim();
                
                // Formata a linha
                resultado.append(String.format("%-15s : %s\n", chave, valor));
            }
        }
        
        return resultado.toString();
    }

