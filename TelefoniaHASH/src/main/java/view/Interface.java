    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String numeroCelular =  txtNumero.getText().trim();
        
        if (numeroCelular.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite um número de celular", "campo vazio", JOptionPane.WARNING_MESSAGE);
            txtNumero.requestFocus();
            return;
        }
        
        if (sessoes.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nenhuma sessão carregada!\nCarregue um arquivo primeiro.", "Lista vazia", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        Sessao sessaoEncontrada = sessoes.get(numeroCelular);
        
        if (sessaoEncontrada != null) {
            //Formata os dados para exibição
            String resultado = "[OK] SESSÃO ENCONTRADA\n";
            resultado += "=========================================\n\n";
            resultado += "Número de Celular: " + sessaoEncontrada.numeroCelular + "\n\n";
            resultado += "Dados da Sessão:\n";
            resultado += formatarJSON(sessaoEncontrada.dadosSessao);
            
            txaResultados.setText(resultado);
        } else {
            String resultado = "[FALHA] SESSÃO NÃO ENCONTRADA\n\n";
            resultado += "O número " + numeroCelular + " não foi encontrado na base de dados.\n";
            resultado += "Verifique se o número está correto e tente novamente.";
            
            txaResultados.setText(resultado);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

