    private void btnCarregarFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCarregarFileActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Selecione o arquivo de sessões");
        
        // Filtro para arquivos .txt
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Arquivos de texto (*.txt)", "txt");
        fileChooser.setFileFilter(filter);
        
        int resultado = fileChooser.showOpenDialog(this);
        
        if (resultado == JFileChooser.APPROVE_OPTION) {
            String caminho = fileChooser.getSelectedFile().getAbsolutePath();
            
            try {
                // Limpa sessões anteriores
                sessoes.clear();
                
                // Carrega o arquivo usando a classe Arquivo
                File.carregarArquivo(sessoes, caminho);
                
                // Atualiza o contador de registros
                lblNumerosCarregados.setText(String.valueOf(sessoes.size()));
                
                // Mensagem de sucesso
                JOptionPane.showMessageDialog(
                    this, "Arquivo carregado com sucesso!\n" + sessoes.size() + " registros foram importados.",
                    "Sucesso", JOptionPane.INFORMATION_MESSAGE
                );
                
                // Limpa os campos
                txtNumero.setText("");
                txaResultados.setText("");
                txtNumero.requestFocus();
                
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Erro ao carregar o arquivo:\n" + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnCarregarFileActionPerformed

